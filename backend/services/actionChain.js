/**
 * Generate vendor-specific patch command
 */
function generatePatchCommand(triageOutput) {
  const products = triageOutput.affected_products;
  const commands = {};

  for (const product of products) {
    const vendor = product.vendor.toLowerCase();
    
    if (vendor.includes('linux') || vendor.includes('ubuntu') || vendor.includes('debian')) {
      commands.debian_ubuntu = `# Update ${product.product}
sudo apt-get update
sudo apt-get install --only-upgrade ${product.product}
sudo systemctl restart ${product.product}`;
    } else if (vendor.includes('redhat') || vendor.includes('centos')) {
      commands.redhat_centos = `# Update ${product.product}
sudo yum update ${product.product}
sudo systemctl restart ${product.product}`;
    } else if (vendor.includes('windows') || vendor.includes('microsoft')) {
      commands.windows = `# Update ${product.product} (PowerShell)
# Download patch from vendor
# Install-WindowsUpdate -KBArticleID <KB_NUMBER> -AcceptAll
Restart-Service -Name "${product.product}" -Force`;
    } else {
      commands.generic = `# Generic update command for ${product.vendor} ${product.product}
# Consult vendor documentation for specific patch procedure
# Verify patch authenticity before installation`;
    }
  }

  return {
    commands: commands,
    notes: 'Always test patches in staging environment first. Verify patch authenticity using vendor signatures.',
    estimated_downtime: triageOutput.severity === 'CRITICAL' ? '5-15 minutes' : '10-30 minutes'
  };
}


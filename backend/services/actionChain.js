/**
 * Generate network ACL entry
 */
function generateNetworkACL(triageOutput) {
  const isNetworkBased = triageOutput.attack_vector === 'Network';

  if (!isNetworkBased) {
    return {
      applicable: false,
      reason: `Attack vector is ${triageOutput.attack_vector}, not Network-based`
    };
  }

  return {
    applicable: true,
    iptables: `# Block suspicious traffic for ${triageOutput.cve_id}
# Add to iptables rules
iptables -A INPUT -p tcp --dport <vulnerable_port> -m state --state NEW -m recent --set --name ${triageOutput.cve_id.replace(/[^a-zA-Z0-9]/g, '_')}
iptables -A INPUT -p tcp --dport <vulnerable_port> -m state --state NEW -m recent --update --seconds 60 --hitcount 10 --name ${triageOutput.cve_id.replace(/[^a-zA-Z0-9]/g, '_')} -j DROP`,
    cloud_acl: {
      aws_security_group: {
        action: 'Deny',
        protocol: 'TCP',
        port_range: '<vulnerable_port>',
        source: '0.0.0.0/0',
        description: `Block ${triageOutput.cve_id} exploitation attempts`
      },
      azure_nsg: {
        name: `Block-${triageOutput.cve_id}`,
        priority: 100,
        direction: 'Inbound',
        access: 'Deny',
        protocol: 'TCP',
        destination_port_range: '<vulnerable_port>'
      }
    },
    deployment_notes: 'Replace <vulnerable_port> with actual service port. Test connectivity before production deployment.'
  };
}


/**
 * Calculate containment confidence score
 */
function calculateContainmentConfidence(triageOutput) {
  let confidence = 0.7; // Base confidence

  // Boost for network-based attacks (easier to contain)
  if (triageOutput.attack_vector === 'Network') {
    confidence += 0.15;
  }

  // Boost for high triage confidence
  if (triageOutput.triage_confidence >= 0.9) {
    confidence += 0.1;
  }

  // Reduce for complex attacks
  if (triageOutput.attack_complexity === 'High') {
    confidence -= 0.1;
  }

  return Math.min(0.95, Math.max(0.5, confidence));
}

